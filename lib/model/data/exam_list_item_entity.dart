  String get eaxmName => _eaxmName;

  num get score => _score;

  num get answerNumber => _answerNumber;

  dynamic get finishAnswerNumber => _finishAnswerNumber;

  dynamic get maxScore => _maxScore;

  /// 0是没做过，1是做过了，3是完成了
