  String get paperUuid => _paperUuid;

  int get responseTime => _responseTime;

  String get instructions => _instructions;

  dynamic get examBeginTime => _examBeginTime;

  dynamic get examEndTime => _examEndTime;

  int get effectiveNumberType => _effectiveNumberType;

  int get questionNumber => _questionNumber;

  dynamic get typeId => _typeId;

  dynamic get state => _state;

  dynamic get sort => _sort;

  dynamic get createTime => _createTime;

  List<ExamGroupCountEntity> get examGroupCountList => _examGroupCountList;

  ExamListItemEntity(
      {String eaxmName,
      int score,
      int answerNumber,
      dynamic finishAnswerNumber,
      dynamic maxScore,
      int status,
      int examId,
      String paperUuid,
      int responseTime,
      String instructions,
      dynamic examBeginTime,
      dynamic examEndTime,
      int effectiveNumberType,
      int questionNumber,
      dynamic typeId,
      dynamic state,
      dynamic sort,
      dynamic createTime,
      List<ExamGroupCountEntity> examGroupCountList,int timeType,}) {
    _eaxmName = eaxmName;
    _score = score;
    _answerNumber = answerNumber;
    _finishAnswerNumber = finishAnswerNumber;
    _maxScore = maxScore;
    _status = status;
    _examId = examId;
    _paperUuid = paperUuid;
    _responseTime = responseTime;
    _instructions = instructions;
    _examBeginTime = examBeginTime;
    _examEndTime = examEndTime;
    _effectiveNumberType = effectiveNumberType;
    _questionNumber = questionNumber;
    _typeId = typeId;
    _state = state;
    _sort = sort;
    _createTime = createTime;
    _examGroupCountList = examGroupCountList;
    _timeType=timeType;
  }

  ExamListItemEntity.fromJson(dynamic json) {
    _eaxmName = json["eaxmName"];
    _score = json["score"];
    _answerNumber = json["answerNumber"];
    _finishAnswerNumber = json["finishAnswerNumber"];
    _maxScore = json["maxScore"];
    _status = json["status"];
    _examId = json["examId"];
    _paperUuid = json["paperUuid"];
    _responseTime = json["responseTime"];
    _instructions = json["instructions"];
    _examBeginTime = json["examBeginTime"];
    _examEndTime = json["examEndTime"];
    _effectiveNumberType = json["effectiveNumberType"];
    _questionNumber = json["questionNumber"];
    _typeId = json["typeId"];
    _state = json["state"];
    _sort = json["sort"];
    _createTime = json["createTime"];
    _timeType = json["timeType"];
    if (json["examGroupCountList"] != null) {
      _examGroupCountList = [];
      json["examGroupCountList"].forEach((v) {
        _examGroupCountList.add(ExamGroupCountEntity.fromJson(v));
      });
    }
  }

  Map<String, dynamic> toJson() {
    var map = <String, dynamic>{};
    map["eaxmName"] = _eaxmName;
    map["score"] = _score;
    map["answerNumber"] = _answerNumber;
    map["finishAnswerNumber"] = _finishAnswerNumber;
    map["maxScore"] = _maxScore;
    map["status"] = _status;
    map["examId"] = _examId;
    map["paperUuid"] = _paperUuid;
    map["responseTime"] = _responseTime;
    map["instructions"] = _instructions;
    map["examBeginTime"] = _examBeginTime;
    map["examEndTime"] = _examEndTime;
    map["effectiveNumberType"] = _effectiveNumberType;
    map["questionNumber"] = _questionNumber;
    map["typeId"] = _typeId;
    map["state"] = _state;
    map["sort"] = _sort;
    map["createTime"] = _createTime;
    map["timeType"] = _timeType;
    if (_examGroupCountList != null) {
      map["examGroupCountList"] =
          _examGroupCountList.map((v) => v.toJson()).toList();
    }
    return map;
  }
